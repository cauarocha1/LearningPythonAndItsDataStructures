# accessing data in dictionaries
data = {"name": "Guilherme", "age": 28, "phone": "3333-1234"}

print(data["name"])  # "Guilherme"
print(data["age"])  # 28
print(data["phone"])  # "3333-1234"

data["name"] = "Maria"
data["age"] = 18
data["phone"] = "9988-1781"

print(data)  # {"name": "Maria", "age": 18, "phone": "9988-1781"}
